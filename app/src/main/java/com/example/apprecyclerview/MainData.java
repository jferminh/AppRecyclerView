package com.example.apprecyclerview;

public class MainData {
    int image;
    String name;
    String info;

    public MainData(int image, String name, String info) {
        this.image = image;
        this.name = name;
        this.info = info;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getInfo() {
        return info;
    }
}
